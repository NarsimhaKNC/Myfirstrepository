package package3;

public class Switchcase {
	public static void main(String[] args){
		String Case="monday";
		switch(Case){
		case "sunday":
			System.out.print("holiday");
			break;
		case "saturday":
			System.out.print("holiday");
			break;
			default:
				System.out.print("working day");
			
			
		
		
		
		}
	}


}